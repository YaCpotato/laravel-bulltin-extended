     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newPost = new Post;
        $newPost->UserId = 1;
        $newPost->level = 1;
        $newPost->nextId = 1;
        $newPost->content = $request->content;
        $newPost->save();
        $posts = Post::latest()->get();
        return view('post/index', compact('posts'));
    }

